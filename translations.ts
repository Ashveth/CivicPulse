/**
 * Formats a date using the provided language's locale.
 */
export const formatDate = (timestamp: number, language: Language): string => {
  const locale = TRANSLATIONS[language].locale;
  return new Date(timestamp).toLocaleDateString(locale, {
    year: 'numeric',
    month: 'short',
    day: 'numeric'
  });
};

/**
 * Formats a number using the provided language's locale.
 */
export const formatNumber = (num: number, language: Language, options?: Intl.NumberFormatOptions): string => {
  const locale = TRANSLATIONS[language].locale;
  return new Intl.NumberFormat(locale, options).format(num);
};

/**
 * Translates an enum value to the target language.
 */
export const translateEnum = (value: string, type: 'status' | 'severity' | 'issueType', language: Language): string => {
  const mapping = TRANSLATIONS[language].enums[type];
  return mapping[value] || value;
};
